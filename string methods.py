str1="pratik narayan garad"
print(str1.upper())
# LOWER():
str1="PRATIK NARAYAN GARAD"
print(str1.lower())
#RSTRIP():
str1="Hellow::::::::"
print(str1.rstrip(":"))
#REPLACE():
str1="YCIS SATARA"
print(str1.replace("YCIS","FORENSIC"))
# SPLIT():
a = "!!!Harry!! !!!!!!!!! Harry"
print(a.split(" "))
#CAPITALIZE():
str1="pratik narayan garad"
print(str1.capitalize())

